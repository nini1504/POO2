package openclosed.correct;

/**
 *
 * @author fabiola
 */
public class Rectangle implements GeometryFigureInterface {

    private int width;
    private int height;

    public Rectangle(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    @Override
    public double getArea() {
        return this.height * this.width;
    }

}
